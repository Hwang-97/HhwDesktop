-- 수강 더미 100 개는 이미 학생이 사용중이기 때문에 수강 더미를 더 만들거나  쿼리로 종료된 과정의 학생들을 뽑아와야됨.
-- 사실 수강 종료후 직접 하나씩 졸업생 테이블에 추가할 것이 아니면 쿼리작성이 유리할것 같기도 함.
create or REPLACE view vwGraduate
as
select 
    s.student_seq,
    s.name,
    s.ssn,
    s.tel,
    l.finishclassdate,
    s.manager_seq
from tblsugang su inner join tbllclass l on su.lclass_seq = l.lclass_seq 
    inner join tblstudent s on s.student_seq = su.student_seq
    WHERE l.finishclassdate<sysdate; -- 수강 종료일을 현 날짜와 비교

select * from vwgraduate; -- 졸업생 정보와 lclass 수강 종료일을 가져옴




















